    public Boolean CheckHospital(String nameh){
       boolean check = false;
       PreparedStatement st;
       Connection conn;
       ResultSet rs;
       String query = " SELECT * FROM Hospital WHERE h_Name=?";
       try {
           conn = Database.getDefaultConnection();
           st = conn.prepareStatement(query);
           st.setString(1,nameh);
           rs= st.executeQuery();
         if(rs.next()){ 
             check = true;
         }
          st.close();
       } catch (SQLException ex) {
           Logger.getLogger(NewHospital.class.getName()).log(Level.SEVERE, null, ex);
       } 
       return check;
   }

