    private void jInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jInsertActionPerformed
       String city = jCity.getText();       
       String street = jStreet.getText();
       int nstreet = Integer.parseInt(jN.getText());
       int ncap = Integer.parseInt(jCAP.getText());
       String typel = new String("Indoor");
       String nation = new String("Italia");
       String hname = jName.getText();
       
       
       if(street.equals("") || city.equals("") || jN.getText().equals("") || jCAP.getText().equals("") || hname.equals("")){
           JOptionPane.showMessageDialog(null, "Errore. Compilare tutti i campi.");
       }
       else {
           if(CheckHospital(hname)){
               JOptionPane.showMessageDialog(null, "Ospedale giá presente nel Database.");
           }
           else{
            PreparedStatement st;
            Connection conn;
            String query = "INSERT INTO NHOSPITAL(City,Street,s_Number,l_Type,p_Code,Nation,h_Name) VALUES (?,?,?,?,?,?,?)";
           try {
             conn = Database.getDefaultConnection();
             st = conn.prepareStatement(query);
             st.setString(1,city);
             st.setString(2,street);
             st.setInt(3,nstreet);
             st.setString(4,typel);
             st.setInt(5,ncap);
             st.setString(6,nation);
             st.setString(7,hname);
             if(st.executeUpdate()>0){ 
                 JOptionPane.showMessageDialog(null, "Nuovo ospedale aggiunto con successo.");
             }
             st.close();
             } catch (SQLException ex) {
                Logger.getLogger(newLoc.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
       }
    }//GEN-LAST:event_jInsertActionPerformed

