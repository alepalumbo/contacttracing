    private void ViewTable(){
       PreparedStatement st;
       Connection conn;
       ResultSet rs;
       String query = " SELECT * FROM v_Presence";
       model = (DefaultTableModel) pTable.getModel();
       model.setRowCount(0);
       try {
            conn = Database.getDefaultConnection();
              st = conn.prepareStatement(query);
              rs= st.executeQuery();
           
            while(rs.next()){
               String idpr =rs.getString(1);
               String name =rs.getString(2);
               String surname =rs.getString(3);
               String ts = rs.getString(4);
               String te =rs.getString(5);
               String mask =rs.getString(6);
               String city = rs.getString(7);
               String street =rs.getString(8);
               String n =rs.getString(9);
               model.addRow(new Object[]{idpr,name,surname,ts,te,mask,city,street,n});
                
            }
            st.close();
        } catch (SQLException ex) {
          Logger.getLogger(newTest.class.getName()).log(Level.SEVERE, null, ex);
        }    
    }
