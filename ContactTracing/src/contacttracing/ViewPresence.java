    private void jDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDeleteActionPerformed
        int idpr = 0;
        boolean n = true;
        try{
            idpr = Integer.parseInt(pTable.getValueAt(pTable.getSelectedRow(),0).toString());
        }
        catch(ArrayIndexOutOfBoundsException exc){
            JOptionPane.showMessageDialog(null, "Errore. Selezionare la riga dell'occorrenza da eliminare.");
            n = false;
        }
        
        if(n){
            PreparedStatement st;
            Connection conn;
            String query = "DELETE FROM Presence WHERE id_PR=?";
        try {
           conn = Database.getDefaultConnection();
           st = conn.prepareStatement(query);
           st.setInt(1,idpr);
           if(st.executeUpdate()>0){ 
            JOptionPane.showMessageDialog(null, "Occorrenza eliminata correttamente.");
            ViewTable();
         }
             st.close();
             } catch (SQLException ex) {
           Logger.getLogger(newLoc.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jDeleteActionPerformed

