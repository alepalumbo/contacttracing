    private void jInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jInsertActionPerformed
        int idv = getVisit();
        String res = new String();
        
        if(jNegative.isSelected())
            res = "negative";
        if(jIGM.isSelected())
            res =  "igm";
        if(jIGG.isSelected())
            res = "igg";
        
        PreparedStatement st;
        Connection conn;
        String query = "INSERT INTO Serological(id_VSE,Results) VALUES (?,?)";
        try {
           conn = Database.getDefaultConnection();
           st = conn.prepareStatement(query);
           st.setInt(1,idv);
           st.setString(2,res);
           if(st.executeUpdate()>0){
               if(res.equals("igm"))
                    JOptionPane.showMessageDialog(null, "Test sierologico eseguito correttamente.\nPer accertamenti si consiglia di eseguire un tampone naso-faringeo.");
               else
                   JOptionPane.showMessageDialog(null, "Test sierologico eseguito correttamente.");
            dispose();
         }
             st.close();
             } catch (SQLException ex) {
           Logger.getLogger(newSerological .class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_jInsertActionPerformed

